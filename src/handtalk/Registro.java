    public boolean verificarCorreo(String Correo){
        
        Pattern pat = Pattern.compile("([a-z0-9]+(\\.?[a-z0-9])*)+@(([a-z]+)\\.([a-z]+))+");
    Matcher mat = pat.matcher(Correo);
    
    return mat.find();
    
    }
    
