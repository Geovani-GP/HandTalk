    public boolean verificarIgualdad(String nuevoCorreo){
    try (Scanner scanner = new Scanner(new File("registros.txt"))) {
        while (scanner.hasNextLine()) {
            String linea = scanner.nextLine();
            if (linea.equalsIgnoreCase(nuevoCorreo)) {
                return true; // El correo ya existe en el archivo
            }
        }
    } catch (FileNotFoundException e) {
        e.printStackTrace();
    }
    return false; // El correo no existe en el archivo
}
    
