    public void habilitarBoton() {

        String contraseña = new String(pswContraseñaCON.getPassword());
        String confirmacion = new String(pswContraseña.getPassword());
        
        //Nombre
        if (txtNombre.getText().isEmpty()){
            lblNombre2.setText("*Campo Obligatorio*");
        }else if(!verificarNombre(txtNombre.getText())){
            lblNombre2.setText("*Datos no validos*");
        }else{
            lblNombre2.setText("");
        }
        
        //Apellidos
        if (txtApellidos.getText().isEmpty()){
            lblApellidos2.setText("*Campo Obligatorio*");
        }else if(!verificarApellidos(txtApellidos.getText())){
            lblApellidos2.setText("*Datos no validos*");
        }else{
            lblApellidos2.setText("");
        }
        
        //Correo
        if (txtCorreo.getText().isEmpty()){
            lblCorreo2.setText("*Campo Obligatorio*");
        }else if(!verificarCorreo(txtCorreo.getText())){
            lblCorreo2.setText("*Correo Invalido*");
        }else{
            lblCorreo2.setText("");
        }
        
        // Contraseña
        if (pswContraseñaCON.getText().isEmpty()) {
            lblContraseñaCON2.setText("Campo Obligatorio");
        } else if (!VerificarContraseñas(contraseña, confirmacion)) {
                lblContraseñaCON2.setText("Las contraseñas no coinciden");
        } else {
                lblContraseñaCON2.setText("");
        }
        

        // Confirmar contraseña
        if (pswContraseña.getText().isEmpty()) {
            lblContraseña2.setText("Campo Obligatorio");
        } else {
            lblContraseña2.setText("");
        }

        if (!txtNombre.getText().isEmpty() && !txtApellidos.getText().isEmpty()
                && !txtCorreo.getText().isEmpty() && !pswContraseña.getText().isEmpty()
                && !pswContraseñaCON.getText().isEmpty()&& verificarCorreo(txtCorreo.getText())
                && contraseña.equals(confirmacion)&& verificarNombre(txtNombre.getText())
                && verificarApellidos(txtApellidos.getText())){

            btnIniciarSesion.setEnabled(true);
        } else {

            btnIniciarSesion.setEnabled(false);

        }
        
    }

